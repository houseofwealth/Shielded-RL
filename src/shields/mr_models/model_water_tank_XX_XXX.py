Time = IntSort()
global_props = [] # globals() is used by Python

# State variables
o, oX, p, pX = Consts('o oX p pX', Time)  #for open XX (o) and XXXX (p)
c, cX, d, dX = Consts('c cX d dX', Time)  #for close XX (c) and XXX (d)
tank, tankX =  Reals('tank tankX')

state  = [o, c, p, d, tank]
stateX = [oX,cX,pX,dX,tankX]
subst  = list(zip(state, stateX))

# Env variables
inn = Real('inn')
out = Real('out')

# Control variable
open = Bool('open')
NUM_NEXTS_XX     = 2
NUM_NEXTS_XXX    = 3

# Node
singleton = SimpleNamespace(
    name         = 'singleton',
    vars         = state,
    postVars     = stateX,
    subst        = subst,
    invariant    = And(0 <= o, o <= NUM_NEXTS_XX, 0 <= c, c <= NUM_NEXTS_XX,
                       0 <= p, p <= NUM_NEXTS_XXX, 0 <= d, d <= NUM_NEXTS_XXX), #True
    stateInvDelta = True,   # initialised; updated by initInvsWithSafetyProps
)

# Arcs/Transitions
"""open ∧ Xclosed → XXclosed  tracked by o and c """
Open_XX = And(Implies(And(o == 0, open), oX == 1),   # open @ o=0 → o'=1
              Implies(And(o == 1, open), oX == 1),   # open  @ o=1 → o'=1 (stay)
              Implies(And(c == 0, open), cX == 0),   # open  @ c=0 → c'=0
              Implies(And(c == 1, open), cX == 2),   # open  @ c=1 → c'=2 (obligation)
              Implies(c == 2, And(open, cX == 0)))        # must open @ c=2

"""open ∧ Xclosed → XXXclosed  tracked by p and d"""
Open_XXX =And(Implies(And(p == 0, open), pX == 1),  # open @ p=0 → p'=1 
              Implies(And(p == 1, open), pX == 1),  # open @ p=1 → p'=1 (stay)
              Implies(p == 2, pX == 3),             # always p=2 → p'=3
              Implies(And(d == 0, open), dX == 0),  # open @ d=0 → d'=0
              Implies(And(d == 1, open), dX == 2),  # open @ d=1 → d'=2 (obligation)
              Implies(d == 2, dX == 3),             # always @ d=2 → d'=3
              Implies(d == 3, And(open, dX == 0)))

openAction = SimpleNamespace(
    name        = "openAction",
    actionPred  = And(tankX == tank + inn - out, Open_XX, Open_XXX),
    envVars     = [inn, out],
    envPred     = And(1 <= inn, inn <= 2, 0 <= out, out <= 1),
    controlVars = [open],
    controlPred = open,
    strengthening = True,   # initialised; may be updated by mkWPC
    precNode    = singleton,
    postNode    = singleton,
)

Close_XX = And(Implies(And(c == 0, Not(open)), cX == 1),   # close @ c=0 → c'=1
               Implies(And(o == 0, Not(open)), oX == 0),   # close @ o=0 → o'=0
               Implies(And(o == 1, Not(open)), oX == 2),   # close @ o=1 → o'=2
               Implies(And(c == 1, Not(open)), cX == 1),   # close @ c=1 → c'=1 (stay)
               Implies(o == 2, And(Not(open), oX == 0)))   # close @ o=2, obligation fulfilled → reset

Close_XXX = And(Implies(And(d == 0, Not(open)), dX == 1),   # close @ d=0 → d'=1
                Implies(And(d == 1, Not(open)), dX == 1),   # close @ d=1 → d'=1 (stay)
                Implies(d == 2, dX == 3),                   # duplicate, here for unformity
                Implies(And(p == 0, Not(open)), pX == 0),   # close @ p=0 → p'=0
                Implies(And(p == 1, Not(open)), pX == 2),   # close @ p=1 → p'=2
                Implies(p == 2, pX == 3),                   # duplicate of abobe
                Implies(p == 3, And(Not(open), pX == 0)))   # close @ p=2, obligation fulfilled → reset

"""closed ∧ Xopen → XXopen    tracked by c (c=1: just closed, c=2: [close,open] obligation)
 """
closeAction = SimpleNamespace(
    name        = "closeAction",
    actionPred  = And(tankX == tank - out, Close_XX, Close_XXX),
    envVars     = [inn, out],
    envPred     = And(1 <= inn, inn <= 2, 0 <= out, out <= 1),
    controlVars = [open],
    controlPred = Not(open),
    strengthening = True,   # initialised; may be updated by mkWPC
    precNode    = singleton,
    postNode    = singleton,
)

transitions = [openAction, closeAction]

